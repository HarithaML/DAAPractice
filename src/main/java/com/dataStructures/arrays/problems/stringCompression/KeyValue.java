package com.dataStructures.arrays.problems.stringCompression;

public class KeyValue {
    char c;
    int x;

    public KeyValue(char c, int x) {
        this.c = c;
        this.x = x;
    }

    public char getC() {
        return c;
    }

    public void setC(char c) {
        this.c = c;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }
}
